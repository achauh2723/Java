// Printing series using loop.

package packages;

import java.util.Scanner;

public class loop {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter the number 10");
		int i = sc.nextInt(); 
		for(i=0;i<=10;i++) {
			System.out.println(i);
		}
	}

}
